#ifndef STD_TYPES_H_
#define STD_TYPES_H_

typedef unsigned char      Uint8t;
typedef unsigned short int Uint16t;
typedef unsigned long int  Uint32t;

typedef signed char      Sint8t;
typedef signed short int Sint16t;
typedef signed long int  Sint32t;

typedef float  Fint32t;
typedef double Dint64t;




#endif /* STD_TYPES_H_ */